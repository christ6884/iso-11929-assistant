const translations = { fr, en, de, es };

export function getTranslator(lang: Language): (key: string) => string {
    const langKey = lang || Language.FR;
    const selectedTranslations = translations[langKey] || translations.en;
    return (key: string): string => {
        // Fallback to English if key missing in selected lang, then to key itself
        return selectedTranslations[key] || translations.en[key] || key;
    };
}
