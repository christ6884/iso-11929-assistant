const translations: Record<Language, any> = {
    [Language.FR]: fr,
    [Language.EN]: en,
    [Language.DE]: de,
    [Language.ES]: es,
};

export const getTranslator = (lang: Language) => (key: string): string => {
    const dict = translations[lang] || translations[Language.EN];
    const translation = dict[key] || translations[Language.EN][key] || key;
    return translation;
};

export const getLocalizedNuclideName = (nuclideName: string, t: (key: string) => string): string => {
    // Attempt to translate the element name part of the nuclide string
    // Format expected: "Americium-241 (Am-241)" or "Cobalt-60"
    const parts = nuclideName.split('-');
    if (parts.length > 0) {
        const elementEnglish = parts[0].split(' ')[0]; // "Americium"
        const elementKey = `element_${elementEnglish.toLowerCase()}`;
        const translatedElement = t(elementKey);
        
        if (translatedElement && translatedElement !== elementKey) {
            return nuclideName.replace(elementEnglish, translatedElement);
        }
    }
    return nuclideName;
};
